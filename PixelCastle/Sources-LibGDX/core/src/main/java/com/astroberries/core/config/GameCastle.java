package com.astroberries.core.config;

public class GameCastle {

    private String image;
    private int x;
    private int y;

    public void setImage(String image) {
        this.image = image;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public String getImage() {
        return image;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
